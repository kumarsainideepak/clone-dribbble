let grid = document.querySelector('.grid');
function template(name, logo, badge, numberOfLikes, numberOfViews, image, fadedName){
    let cardDiv = document.createElement('div');
    cardDiv.setAttribute('class', 'card');
    grid.appendChild(cardDiv);

    let cardImgDiv = document.createElement('div');
    cardImgDiv.setAttribute('class', 'card-img');
    cardDiv.append(cardImgDiv);

    let img = document.createElement('img');
    img.setAttribute('src',image);
    img.setAttribute('alt', 'Image');
    cardImgDiv.append(img);

    let imgTitleDiv = document.createElement('div');
    imgTitleDiv.setAttribute('class', 'img-title');
    cardImgDiv.append(imgTitleDiv);

    let brandingDiv = document.createElement('div');
    brandingDiv.setAttribute('id', 'branding');
    let buttonsDiv = document.createElement('div');
    buttonsDiv.setAttribute('id', 'buttons');
    imgTitleDiv.append(brandingDiv);
    imgTitleDiv.append(buttonsDiv);

    let brandingBold = document.createElement('strong');
    brandingBold.textContent = fadedName;
    brandingBold.setAttribute('style','padding-left: 1rem;')
    brandingDiv.append(brandingBold);

    let buttonElement1 = document.createElement('button');
    let buttonElement2 = document.createElement('button');
    buttonElement1.setAttribute('id', 'folder-button');
    buttonElement2.setAttribute('id', 'heart-button');
    buttonsDiv.append(buttonElement1);
    buttonsDiv.append(buttonElement2);
    
    let cardBodyDiv = document.createElement('div');
    cardBodyDiv.setAttribute('class', 'card-body');
    cardDiv.append(cardBodyDiv);

    let cardTitleDiv = document.createElement('div');
    cardTitleDiv.setAttribute('class', 'card-title');
    cardBodyDiv.append(cardTitleDiv);

    let logoImg = document.createElement('img');
    logoImg.setAttribute('src', logo);
    logoImg.setAttribute('alt', 'logo');
    cardTitleDiv.append(logoImg);

    let nameB = document.createElement('b');
    nameB.textContent = name;
    cardTitleDiv.append(nameB);

    let badgeSpan = document.createElement('span');
    badgeSpan.setAttribute('class', 'badge');
    badgeSpan.textContent = badge;
    cardTitleDiv.append(badgeSpan);

    let cardDetailsDiv = document.createElement('div');
    cardDetailsDiv.setAttribute('class','card-details');
    cardBodyDiv.append(cardDetailsDiv);

    let heartSpan = document.createElement('span');
    heartSpan.setAttribute('id', 'heart-icon');
    cardDetailsDiv.append(heartSpan);

    let likeB = document.createElement('b');
    likeB.textContent = numberOfLikes;
    cardDetailsDiv.append(likeB);

    let eyeSpan = document.createElement('span');
    eyeSpan.setAttribute('id', 'eye-icon');
    cardDetailsDiv.append(eyeSpan);

    let viewsB = document.createElement('b');
    viewsB.textContent = numberOfViews+'k';
    cardDetailsDiv.append(viewsB);

}

